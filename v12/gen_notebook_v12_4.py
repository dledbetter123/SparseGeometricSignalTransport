# CELL 11: V12_4Block
# ═══════════════════════════════════════════════════════════════
code(
"class V12_4Block(nn.Module):\n"
"    \"\"\"Geometric transport block. Spectral-native, complex-native.\n"
"\n"
"    spectral_x -> SSM(|spectral_x|) -> context\n"
"    spectral_x -> Interaction(context) -> Transport(context) ->\n"
"    SpectralNorm -> ComplexMLP -> spectral residual -> sparsify\n"
"\n"
"    The Interaction IS the transport network. Context generates the\n"
"    transport geometry. ComplexMLP is residual correction.\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.cfg = cfg\n"
"        self.ssm = SpectralSSM(cfg)\n"
"        self.interact = SpectralInteraction(cfg)\n"
"        self.transport = SpectralTransport(cfg)\n"
"        self.norm = SpectralNorm(cfg.spectral_dim)\n"
"        self.mlp = ComplexMLP(cfg)\n"
"        self.res_gate = nn.Parameter(torch.tensor(0.5))\n"
"\n"
"    def forward(self, spectral_x):\n"
"        cfg = self.cfg\n"
"\n"
"        # 1. SSM reads magnitudes -> context (which modes, how strong)\n"
"        q_t = self.ssm(spectral_x)\n"
"\n"
"        # 2. Context-dependent geometric transport\n"
"        interacted = self.interact(spectral_x, q_t, cfg)\n"
"\n"
"        # 3. Per-mode scaling + phase shift\n"
"        transported = self.transport(interacted, q_t)\n"
"\n"
"        # 4. Complex nonlinear correction\n"
"        normed = self.norm(transported)\n"
"        mlp_out = self.mlp(normed)\n"
"\n"
"        # 5. Spectral residual + enforce sparsity\n"
"        gate = torch.sigmoid(self.res_gate)\n"
"        spectral_out = spectral_x + gate * mlp_out\n"
"        return spectral_sparsify(spectral_out, cfg)"
)

