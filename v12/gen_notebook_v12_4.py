"class V12_4Model(nn.Module):\n"
"    \"\"\"Geometric transport CLM. Deep supervision at blocks 2, 4, 6.\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.cfg = cfg\n"
"        self.embedding = SpectralTokenEmbedding(cfg)\n"
"        self.blocks = nn.ModuleList([V12_4Block(cfg) for _ in range(cfg.n_blocks)])\n"
"        self.final_norm = nn.LayerNorm(cfg.fiber_dim)\n"
"        self.decoder = nn.Sequential(\n"
"            nn.Linear(cfg.fiber_dim, cfg.fiber_dim), nn.SiLU(),\n"
"            nn.Dropout(cfg.dropout),\n"
"            nn.Linear(cfg.fiber_dim, cfg.vocab_size),\n"
"        )\n"
"        weights = torch.zeros(cfg.n_blocks)\n"
"        for i in range(cfg.n_blocks):\n"
"            if (i + 1) % 2 == 0:\n"
"                weights[i] = (i + 1) / cfg.n_blocks\n"
"        weights[-1] = 1.0\n"
"        self.register_buffer('block_loss_weights', weights)\n"
"\n"
"    def _decode(self, spectral_x):\n"
"        \"\"\"spectral -> irfft -> spatial -> logits. The ONLY irfft.\"\"\"\n"
"        spatial = spectral_to_spatial(spectral_x, self.cfg)\n"
"        return self.decoder(self.final_norm(spatial))\n"
"\n"
"    def forward(self, token_ids):\n"
"        cfg = self.cfg\n"
"        spectral_x = self.embedding(token_ids)\n"
"        intermediate_logits = []\n"
"        for i, block in enumerate(self.blocks):\n"
"            spectral_x = block(spectral_x)\n"
"            if self.block_loss_weights[i] > 0:\n"
"                logits = self._decode(spectral_x)[:, :-1, :]\n"
"                intermediate_logits.append((logits, self.block_loss_weights[i]))\n"
"        sp = (spectral_x.abs() < 1e-6).float().mean().item()\n"
"        return intermediate_logits[-1][0], {\n"
"            'spectral_sparsity': sp,\n"
"            'intermediate_logits': intermediate_logits,\n"
"        }\n"
"\n"
"\n"
"# -- GPT-Nano ---------------------------------------------------------------\n"
"\n"
"class GPTNano(nn.Module):\n"
"    def __init__(self, vocab_size=65, n_embd=128, n_head=4, n_layer=12,\n"
"                 block_size=512, dropout=0.1):\n"
"        super().__init__()\n"
"        self.block_size = block_size\n"
"        self.tok_emb = nn.Embedding(vocab_size, n_embd)\n"
"        self.pos_emb = nn.Embedding(block_size, n_embd)\n"
"        self.drop = nn.Dropout(dropout)\n"
"        self.blocks = nn.ModuleList()\n"
"        for _ in range(n_layer):\n"
"            self.blocks.append(nn.ModuleDict({\n"
"                'ln1': nn.LayerNorm(n_embd),\n"
"                'attn_qkv': nn.Linear(n_embd, 3 * n_embd),\n"
"                'attn_proj': nn.Linear(n_embd, n_embd),\n"
"                'ln2': nn.LayerNorm(n_embd),\n"
"                'mlp_fc1': nn.Linear(n_embd, 4 * n_embd),\n"
"                'mlp_fc2': nn.Linear(4 * n_embd, n_embd),\n"
"            }))\n"
"        self.ln_f = nn.LayerNorm(n_embd)\n"
"        self.lm_head = nn.Linear(n_embd, vocab_size, bias=False)\n"
"        self.n_head = n_head\n"
"        self.n_embd = n_embd\n"
"        self.register_buffer('causal_mask',\n"
"            torch.tril(torch.ones(block_size, block_size)).view(1,1,block_size,block_size))\n"
"\n"
"    def forward(self, idx):\n"
"        B, T = idx.shape\n"
"        x = self.drop(self.tok_emb(idx) + self.pos_emb(torch.arange(T, device=idx.device)))\n"
"        hd = self.n_embd // self.n_head\n"
"        for blk in self.blocks:\n"
"            h = blk['ln1'](x)\n"
"            qkv = blk['attn_qkv'](h).reshape(B, T, 3, self.n_head, hd)\n"
"            q, k, v = qkv.unbind(2)\n"
"            q, k, v = q.transpose(1,2), k.transpose(1,2), v.transpose(1,2)\n"
"            att = (q @ k.transpose(-2,-1)) * (hd**-0.5)\n"
"            att = att.masked_fill(self.causal_mask[:,:,:T,:T]==0, float('-inf'))\n"
"            y = (F.softmax(att, dim=-1) @ v).transpose(1,2).reshape(B, T, self.n_embd)\n"
"            x = x + blk['attn_proj'](y)\n"
"            x = x + blk['mlp_fc2'](F.gelu(blk['mlp_fc1'](blk['ln2'](x))))\n"
"        return self.lm_head(self.ln_f(x))[:, :-1, :], {}\n"
"\n"
"\n"
"# -- SSM+MLP Only (dense spatial, no spectral domain) -----------------------\n"
"\n"
"class SSMOnlyBlock(nn.Module):\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.A_proj = nn.Linear(cfg.fiber_dim, cfg.context_dim)\n"
"        self.B_proj = nn.Linear(cfg.fiber_dim, cfg.context_dim)\n"
"        self.psi_proj = nn.Linear(cfg.fiber_dim, cfg.context_dim)\n"
"        self.ctx_proj = nn.Linear(cfg.context_dim, cfg.fiber_dim)\n"
"        self.mlp_fc1 = nn.Linear(cfg.fiber_dim, int(cfg.fiber_dim * cfg.mlp_ratio))\n"
"        self.mlp_fc2 = nn.Linear(int(cfg.fiber_dim * cfg.mlp_ratio), cfg.fiber_dim)\n"
"        self.norm1 = nn.LayerNorm(cfg.fiber_dim)\n"
"        self.norm2 = nn.LayerNorm(cfg.fiber_dim)\n"
"        self.drop = nn.Dropout(cfg.dropout)\n"
"        self.gate = nn.Parameter(torch.tensor(0.5))\n"
"\n"
"    def forward(self, x):\n"
"        A = torch.sigmoid(self.A_proj(x))\n"
"        B = torch.sigmoid(self.B_proj(x))\n"
"        psi = self.psi_proj(x)\n"
"        q = parallel_associative_scan(A, B * psi)\n"
"        h = self.norm1(x + self.ctx_proj(q))\n"
"        mlp = self.drop(self.mlp_fc2(F.silu(self.mlp_fc1(self.norm2(h)))))\n"
"        return x + torch.sigmoid(self.gate) * mlp\n"
"\n"
"\n"
"class SSMOnlyModel(nn.Module):\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.cfg = cfg\n"
"        self.emb = nn.Embedding(cfg.vocab_size, cfg.fiber_dim)\n"
"        self.pos = nn.Embedding(cfg.max_seq_len, cfg.fiber_dim)\n"
"        self.drop = nn.Dropout(cfg.dropout)\n"
"        self.blocks = nn.ModuleList([SSMOnlyBlock(cfg) for _ in range(cfg.n_blocks)])\n"
"        self.norm = nn.LayerNorm(cfg.fiber_dim)\n"
"        self.head = nn.Sequential(\n"
"            nn.Linear(cfg.fiber_dim, cfg.fiber_dim), nn.SiLU(),\n"
"            nn.Dropout(cfg.dropout),\n"
"            nn.Linear(cfg.fiber_dim, cfg.vocab_size),\n"
"        )\n"
"\n"
"    def forward(self, ids):\n"
"        x = self.drop(self.emb(ids) + self.pos(torch.arange(ids.size(1), device=ids.device)))\n"
"        for blk in self.blocks:\n"
"            x = blk(x)\n"
"        logits = self.head(self.norm(x))[:, :-1, :]\n"
"        return logits, {'spectral_sparsity': 0.0, 'intermediate_logits': [(logits, 1.0)]}\n"
"\n"
"\n"
