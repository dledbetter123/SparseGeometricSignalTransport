code(
"def spectral_sparsify(x_complex, cfg):\n"
"    \"\"\"Per-subbundle top-k sparsification in spectral domain.\"\"\"\n"
"    shape = x_complex.shape\n"
"    x_subs = x_complex.reshape(*shape[:-1], cfg.n_subbundles, cfg.spectral_half_dim)\n"
"    with torch.no_grad():\n"
"        mags = x_subs.abs()\n"
"        gt_count = (mags.unsqueeze(-1) < mags.unsqueeze(-2)).sum(dim=-1)\n"
"        mask = (gt_count < cfg.spectral_sparsity).float()\n"
"    return (x_subs * mask).reshape(shape)\n"
"\n"
"\n"
"def spectral_to_spatial(x_spectral, cfg):\n"
"    \"\"\"irfft: spectral -> spatial. Used ONLY at decoder.\"\"\"\n"
"    shape = x_spectral.shape[:-1]\n"
"    subs = x_spectral.reshape(*shape, cfg.n_subbundles, cfg.spectral_half_dim)\n"
"    return torch.fft.irfft(subs, n=cfg.subbundle_dim, dim=-1).reshape(*shape, cfg.fiber_dim)\n"
"\n"
"\n"
"def parallel_associative_scan(A, B):\n"
"    _, T, _ = A.shape\n"
"    a, b = A, B\n"
"    for d in range(int(math.ceil(math.log2(T)))):\n"
"        step = 2 ** d\n"
"        if step >= T:\n"
"            break\n"
"        b = torch.cat([b[:, :step, :],\n"
"                        a[:, step:, :] * b[:, :-step, :] + b[:, step:, :]], dim=1)\n"
"        a = torch.cat([a[:, :step, :],\n"
"                        a[:, step:, :] * a[:, :-step, :]], dim=1)\n"
"    return b\n"
"\n"
"\n"
"def modrelu(z, threshold):\n"
"    \"\"\"modReLU: nonlinear on magnitude, transparent to phase.\n"
"    f(z) = ReLU(|z| - threshold) * z / |z|  for |z| > 0\n"
"    Preserves phase, applies soft thresholding to magnitude.\"\"\"\n"
"    mag = z.abs().clamp(min=1e-8)\n"
"    activated_mag = F.relu(mag - threshold)\n"
"    return activated_mag * (z / mag)\n"
"\n"
"\n"
"# Verify modReLU preserves phase\n"
"_z = torch.randn(4, 4) + 1j * torch.randn(4, 4)\n"
"_out = modrelu(_z, 0.5)\n"
"_phase_in = torch.angle(_z)\n"
"_phase_out = torch.angle(_out)\n"
"# Only compare where output is nonzero (active modes)\n"
"_active = _out.abs() > 1e-7\n"
"_err = (_phase_in[_active] - _phase_out[_active]).abs().max().item()\n"
"print(f\"modReLU phase preservation error: {_err:.2e}\")\n"
"print(f\"modReLU kills {(~_active).float().mean():.0%} of modes (threshold=0.5)\")"
)

