code(
"class SpectralTransport(nn.Module):\n"
"    \"\"\"Context-dependent spectral kernel: exp(-D*w^2 - i*w*A).\n"
"    Signed diffusion (tanh). Per-mode scaling and phase shift.\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.cfg = cfg\n"
"        self.D_proj = nn.Linear(cfg.context_dim, cfg.spectral_dim)\n"
"        self.A_proj = nn.Linear(cfg.context_dim, cfg.spectral_dim)\n"
"\n"
"    def forward(self, spectral_x, q_t):\n"
"        cfg = self.cfg\n"
"        D = torch.tanh(self.D_proj(q_t))\n"
"        A = self.A_proj(q_t)\n"
"        w = torch.fft.rfftfreq(cfg.subbundle_dim, d=1.0, device=spectral_x.device)\n"
"        w = w.repeat(cfg.n_subbundles)\n"
"        kernel = torch.exp(-D * w**2 - 1j * w * A)\n"
"        return spectral_x * kernel"
)

