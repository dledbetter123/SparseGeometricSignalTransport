code(
"@torch.no_grad()\n"
"def estimate_loss(model, cfg, is_gpt=False):\n"
"    model.eval()\n"
"    results = {}\n"
"    for name, sd in [('train', train_data), ('val', val_data)]:\n"
"        tot_ce, tot_ok, tot_n, tot_sp = 0., 0, 0, 0.\n"
"        for _ in range(cfg.eval_steps):\n"
"            b = get_batch(sd, cfg)\n"
"            logits, info = model(b)\n"
"            tgt = b[:, 1:]\n"
"            ce = F.cross_entropy(logits.reshape(-1, cfg.vocab_size), tgt.reshape(-1))\n"
"            tot_ce += ce.item()\n"
"            tot_ok += (logits.argmax(-1) == tgt).sum().item()\n"
"            tot_n += tgt.numel()\n"
"            if not is_gpt:\n"
"                tot_sp += info.get('spectral_sparsity', 0.0)\n"
"        n = cfg.eval_steps\n"
"        results[name] = {\n"
"            'ce': tot_ce/n, 'acc': tot_ok/tot_n,\n"
"            'sparsity': tot_sp/n if not is_gpt else 0.0}\n"
"    model.train()\n"
"    return results\n"
"\n"
"\n"
"def train_model(model, cfg, label='V12.4', is_gpt=False, is_ssm_only=False):\n"
"    opt = torch.optim.AdamW(model.parameters(), lr=cfg.learning_rate, weight_decay=0.05)\n"
"    mr = getattr(cfg, 'min_lr', 0) / cfg.learning_rate\n"
"    he = cfg.warmup_steps + getattr(cfg, 'lr_hold_steps', 0)\n"
"\n"
"    def lr_fn(s):\n"
"        if s < cfg.warmup_steps: return s / max(1, cfg.warmup_steps)\n"
"        if s < he: return 1.0\n"
"        p = (s - he) / max(1, cfg.max_steps - he)\n"
"        return max(mr, 0.5 * (1.0 + math.cos(math.pi * p)))\n"
"\n"
"    sched = torch.optim.lr_scheduler.LambdaLR(opt, lr_fn)\n"
"    hist = {'step':[], 'train_ce':[], 'val_ce':[], 'train_acc':[], 'val_acc':[],\n"
"            'train_bpc':[], 'val_bpc':[], 'sparsity':[], 'lr':[],\n"
"            'step_times':[], 'per_step_loss':[]}\n"
"\n"
"    model.train()\n"
"    t0 = time.time()\n"
"    np_ = count_params(model)\n"
"    print(f\"\\nTraining {label}: {np_:,} params\")\n"
"    print(f\"Steps: {cfg.max_steps}, Batch: {cfg.batch_size}, Seq: {cfg.seq_len}\")\n"
"    print('=' * 70)\n"
"\n"
"    for step in range(cfg.max_steps + 1):\n"
"        if step % cfg.eval_interval == 0:\n"
"            r = estimate_loss(model, cfg, is_gpt=is_gpt)\n"
"            tr, vl = r['train'], r['val']\n"
"            hist['step'].append(step)\n"
"            hist['train_ce'].append(tr['ce']); hist['val_ce'].append(vl['ce'])\n"
"            hist['train_acc'].append(tr['acc']); hist['val_acc'].append(vl['acc'])\n"
"            hist['train_bpc'].append(tr['ce']/math.log(2))\n"
"            hist['val_bpc'].append(vl['ce']/math.log(2))\n"
"            hist['sparsity'].append(vl['sparsity'])\n"
"            hist['lr'].append(sched.get_last_lr()[0])\n"
"            sp = f\" | Sp: {vl['sparsity']:.1%}\" if not is_gpt else ''\n"
"            print(f\"[{label}] Step {step:5d} | Train CE: {tr['ce']:.3f} | \"\n"
"                  f\"Val CE: {vl['ce']:.3f} | Val BPC: {vl['ce']/math.log(2):.3f} | \"\n"
"                  f\"Acc: {vl['acc']:.1%}{sp}\")\n"
"\n"
"        if step >= cfg.max_steps: break\n"
"        st = time.time()\n"
"        batch = get_batch(train_data, cfg)\n"
"        opt.zero_grad()\n"
"        logits, info = model(batch)\n"
"        tgt = batch[:, 1:]\n"
"\n"
"        if is_gpt or is_ssm_only:\n"
"            loss = F.cross_entropy(logits.reshape(-1, cfg.vocab_size), tgt.reshape(-1))\n"
"        else:\n"
"            ce, tw = 0., 0.\n"
"            for bl, w in info['intermediate_logits']:\n"
"                ce += w * F.cross_entropy(bl.reshape(-1, cfg.vocab_size), tgt.reshape(-1))\n"
"                tw += w\n"
"            loss = ce / tw\n"
"\n"
"        loss.backward()\n"
"        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)\n"
"        opt.step(); sched.step()\n"
"        hist['step_times'].append(time.time() - st)\n"
"        hist['per_step_loss'].append(loss.item())\n"
"        if step % 500 == 0 and step > 0:\n"
"            print(f\"    avg step time: {np.mean(hist['step_times'][-500:])*1000:.0f}ms\")\n"
"\n"
"    el = time.time() - t0\n"
"    ms = np.mean(hist['step_times']) * 1000\n"
"    print(f\"\\n{label} DONE in {el/60:.1f}min | BPC: {hist['val_bpc'][-1]:.3f} | \"\n"
"          f\"Acc: {hist['val_acc'][-1]:.1%} | {ms:.0f}ms/step\")\n"
"    hist['avg_step_ms'] = ms; hist['n_params'] = np_\n"
"    return hist"
)

