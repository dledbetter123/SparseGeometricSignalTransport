# CELL 10: ComplexMLP (with modReLU)
# ═══════════════════════════════════════════════════════════════
code(
"class ComplexLinear(nn.Module):\n"
"    \"\"\"Linear layer operating on complex tensors: W*z + b, W,b in C.\n"
"    A complex matmul naturally expresses rotations and scalings.\"\"\"\n"
"\n"
"    def __init__(self, in_dim, out_dim):\n"
"        super().__init__()\n"
"        self.W_real = nn.Linear(in_dim, out_dim, bias=False)\n"
"        self.W_imag = nn.Linear(in_dim, out_dim, bias=False)\n"
"        self.bias_real = nn.Parameter(torch.zeros(out_dim))\n"
"        self.bias_imag = nn.Parameter(torch.zeros(out_dim))\n"
"        # Glorot init scaled for complex\n"
"        scale = 1.0 / math.sqrt(2 * in_dim)\n"
"        nn.init.uniform_(self.W_real.weight, -scale, scale)\n"
"        nn.init.uniform_(self.W_imag.weight, -scale, scale)\n"
"\n"
"    def forward(self, z):\n"
"        # (a+bi)(c+di) = (ac-bd) + (ad+bc)i\n"
"        real = self.W_real(z.real) - self.W_imag(z.imag) + self.bias_real\n"
"        imag = self.W_real(z.imag) + self.W_imag(z.real) + self.bias_imag\n"
"        return torch.complex(real, imag)\n"
"\n"
"\n"
"class ComplexMLP(nn.Module):\n"
"    \"\"\"Complex-valued MLP with modReLU activation.\n"
"    Residual correction to the geometric interaction.\n"
"    Operates in complex space — never splits to real+imag.\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        sd = cfg.spectral_dim   # 136 complex\n"
"        hd = cfg.mlp_hidden     # 204 complex\n"
"        self.fc1 = ComplexLinear(sd, hd)\n"
"        self.fc2 = ComplexLinear(hd, sd)\n"
"        self.threshold = nn.Parameter(\n"
"            torch.full((hd,), cfg.modrelu_threshold))\n"
"        self.drop_rate = cfg.dropout\n"
"\n"
"    def forward(self, z):\n"
"        h = modrelu(self.fc1(z), self.threshold)\n"
"        out = self.fc2(h)\n"
"        if self.training and self.drop_rate > 0:\n"
"            mask = torch.rand(out.shape[:-1] + (1,),\n"
"                              device=out.device) > self.drop_rate\n"
"            out = out * mask / (1 - self.drop_rate)\n"
"        return out"
)

