# CELL 8: SpectralInteraction (context-dependent mixing)
# ═══════════════════════════════════════════════════════════════
code(
"class SpectralInteraction(nn.Module):\n"
"    \"\"\"Context-dependent geometric transport in spectral domain.\n"
"\n"
"    The mixing matrix W is not fixed — context generates a low-rank perturbation:\n"
"        W(ctx) = W_base + U(ctx) @ V(ctx)\n"
"    where U, V are rank-r projections from the SSM context.\n"
"\n"
"    This means the transport geometry itself changes per token, per position.\n"
"    Context doesn't just scale output — it reshapes HOW modes couple.\n"
"\n"
"    Two levels:\n"
"    1. Intra-subbundle: context-dependent complex mixing via rank-r perturbation\n"
"    2. Cross-subbundle: fixed 8x8 coupling, context gates which subbundles participate\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        K = cfg.n_subbundles           # 8\n"
"        shd = cfg.spectral_half_dim    # 17\n"
"        r = cfg.interaction_rank       # 2\n"
"        cd = cfg.context_dim           # 128\n"
"        self.K, self.shd, self.r = K, shd, r\n"
"\n"
"        # Base mixing matrices (learned, complex)\n"
"        self.intra_real = nn.Parameter(\n"
"            torch.eye(shd).unsqueeze(0).repeat(K, 1, 1) +\n"
"            torch.randn(K, shd, shd) * 0.02)\n"
"        self.intra_imag = nn.Parameter(torch.randn(K, shd, shd) * 0.02)\n"
"\n"
"        # Context -> low-rank perturbation of intra-subbundle mixing\n"
"        # U: (B,T,128) -> (B,T, K*shd*r) real + imag\n"
"        # V: (B,T,128) -> (B,T, K*r*shd) real + imag\n"
"        self.ctx_U_real = nn.Linear(cd, K * shd * r)\n"
"        self.ctx_U_imag = nn.Linear(cd, K * shd * r)\n"
"        self.ctx_V_real = nn.Linear(cd, K * r * shd)\n"
"        self.ctx_V_imag = nn.Linear(cd, K * r * shd)\n"
"        # Scale down initial perturbation\n"
"        for p in [self.ctx_U_real, self.ctx_U_imag, self.ctx_V_real, self.ctx_V_imag]:\n"
"            nn.init.normal_(p.weight, std=0.01)\n"
"            nn.init.zeros_(p.bias)\n"
"\n"
"        # Cross-subbundle: fixed coupling matrix + context gates participation\n"
"        self.cross = nn.Parameter(\n"
"            torch.eye(K).unsqueeze(0).repeat(shd, 1, 1) +\n"
"            torch.randn(shd, K, K) * 0.02)\n"
"        self.ctx_cross_gate = nn.Linear(cd, K)\n"
"        nn.init.zeros_(self.ctx_cross_gate.bias)\n"
"\n"
"        self.gate = nn.Parameter(torch.tensor(-2.0))\n"
"\n"
"    def forward(self, spectral_x, q_t, cfg):\n"
"        K, shd, r = self.K, self.shd, self.r\n"
"        shape = spectral_x.shape[:-1]  # (B, T)\n"
"        subs = spectral_x.reshape(*shape, K, shd)\n"
"\n"
"        # --- Intra-subbundle: W_base + U(ctx) @ V(ctx) ---\n"
"        W_base = torch.complex(self.intra_real, self.intra_imag)  # (K, shd, shd)\n"
"        U = torch.complex(\n"
"            self.ctx_U_real(q_t).reshape(*shape, K, shd, r),\n"
"            self.ctx_U_imag(q_t).reshape(*shape, K, shd, r))     # (B,T,K,shd,r)\n"
"        V = torch.complex(\n"
"            self.ctx_V_real(q_t).reshape(*shape, K, r, shd),\n"
"            self.ctx_V_imag(q_t).reshape(*shape, K, r, shd))     # (B,T,K,r,shd)\n"
"        # Factored form: (W_base + UV)x = W_base*x + U*(V*x)\n"
"        # Never materialize the full (B,T,K,17,17) matrix\n"
"        base_mixed = torch.einsum('...ks,ksd->...kd', subs, W_base)  # (B,T,K,shd)\n"
"        Vx = torch.einsum('...krs,...ks->...kr', V, subs)             # (B,T,K,r) tiny\n"
"        UVx = torch.einsum('...ksr,...kr->...ks', U, Vx)              # (B,T,K,shd)\n"
"        mixed = base_mixed + UVx                                       # (B,T,K,shd)\n"
"        # --- Cross-subbundle: fixed C, context gates which subbundles participate ---\n"
"        cross_gate = torch.sigmoid(\n"
"            self.ctx_cross_gate(q_t)).unsqueeze(-1)                # (B,T,K,1)\n"
"        gated_mixed = mixed * cross_gate                           # gate before coupling\n"
"\n"
"        gated_t = gated_mixed.transpose(-1, -2)                   # (B,T,shd,K)\n"
"        coupled_t = torch.einsum('...fk,fkj->...fj',\n"
"                                 gated_t, self.cross.to(gated_t.dtype))  # (B,T,shd,K)\n"
"        coupled = coupled_t.transpose(-1, -2)                     # (B,T,K,shd)\n"
"\n"
"        g = torch.sigmoid(self.gate)\n"
"        return (subs + g * (coupled - subs)).reshape(*shape, K * shd)"
)

