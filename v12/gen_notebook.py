"        return self.decoder(self.final_norm(spatial))\n"
"\n"
"    def forward(self, token_ids):\n"
"        B, T = token_ids.shape\n"
"        cfg = self.cfg\n"
"        spectral_x = self.embedding(token_ids)\n"
"        intermediate_logits = []\n"
"        for i, block in enumerate(self.blocks):\n"
"            spectral_x = block(spectral_x)\n"
"            if self.block_loss_weights[i] > 0:\n"
