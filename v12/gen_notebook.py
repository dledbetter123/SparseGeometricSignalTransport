"        self.drop = nn.Dropout(dropout)\n"
"        self.blocks = nn.ModuleList()\n"
"        for _ in range(n_layer):\n"
"            self.blocks.append(nn.ModuleDict({\n"
"                'ln1': nn.LayerNorm(n_embd),\n"
"                'attn_qkv': nn.Linear(n_embd, 3 * n_embd),\n"
"                'attn_proj': nn.Linear(n_embd, n_embd),\n"
"                'ln2': nn.LayerNorm(n_embd),\n"
"                'mlp_fc1': nn.Linear(n_embd, 4 * n_embd),\n"
"                'mlp_fc2': nn.Linear(4 * n_embd, n_embd),\n"
"            }))\n"
"        self.ln_f = nn.LayerNorm(n_embd)\n"
"        self.lm_head = nn.Linear(n_embd, vocab_size, bias=False)\n"
"        self.n_head = n_head\n"
"        self.n_embd = n_embd\n"
"        causal_mask = torch.tril(torch.ones(block_size, block_size))\n"
"        self.register_buffer('causal_mask', causal_mask.view(1, 1, block_size, block_size))\n"
"\n"
"    def forward(self, idx):\n"
"        B, T = idx.shape\n"
"        pos = torch.arange(T, device=idx.device)\n"
"        x = self.drop(self.tok_emb(idx) + self.pos_emb(pos))\n"
"        head_dim = self.n_embd // self.n_head\n"
"        for block in self.blocks:\n"
"            h = block['ln1'](x)\n"
"            qkv = block['attn_qkv'](h).reshape(B, T, 3, self.n_head, head_dim)\n"
"            q, k, v = qkv.unbind(2)\n"
"            q, k, v = q.transpose(1, 2), k.transpose(1, 2), v.transpose(1, 2)\n"
"            att = (q @ k.transpose(-2, -1)) * (head_dim ** -0.5)\n"
"            att = att.masked_fill(self.causal_mask[:, :, :T, :T] == 0, float('-inf'))\n"
"            att = F.softmax(att, dim=-1)\n"
"            y = (att @ v).transpose(1, 2).reshape(B, T, self.n_embd)\n"
"            x = x + block['attn_proj'](y)\n"
"            h = block['ln2'](x)\n"
"            x = x + block['mlp_fc2'](F.gelu(block['mlp_fc1'](h)))\n"
"        logits = self.lm_head(self.ln_f(x))\n"
"        return logits[:, :-1, :], {}\n"
