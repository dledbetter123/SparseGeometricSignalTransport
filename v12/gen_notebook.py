code(
"@torch.no_grad()\n"
"def estimate_loss(model, cfg, is_gpt=False):\n"
"    model.eval()\n"
"    results = {}\n"
"    for name, sd in [(\"train\", train_data), (\"val\", val_data)]:\n"
"        tot_ce, tot_ok, tot_n, tot_sp = 0., 0, 0, 0.\n"
"        for _ in range(cfg.eval_steps):\n"
"            b = get_batch(sd, cfg)\n"
"            logits, info = model(b)\n"
"            tgt = b[:, 1:]\n"
"            ce = F.cross_entropy(logits.reshape(-1, cfg.vocab_size), tgt.reshape(-1))\n"
"            tot_ce += ce.item()\n"
"            tot_ok += (logits.argmax(-1) == tgt).sum().item()\n"
"            tot_n += tgt.numel()\n"
"            if not is_gpt:\n"
"                tot_sp += info.get(\"spectral_sparsity\", 0.0)\n"
"        n = cfg.eval_steps\n"
"        results[name] = {\n"
"            \"ce\": tot_ce/n, \"acc\": tot_ok/tot_n,\n"
"            \"sparsity\": tot_sp/n if not is_gpt else 0.0,\n"
"        }\n"
"    model.train()\n"
"    return results\n"
"\n"
"\n"
"def train_model(model, cfg, label=\"V12.2\", is_gpt=False, is_ssm_only=False):\n"
"    \"\"\"Train loop for V12.2, GPT, and SSM+MLP models.\"\"\"\n"
"    optimizer = torch.optim.AdamW(model.parameters(), lr=cfg.learning_rate, weight_decay=0.05)\n"
"\n"
"    min_ratio = getattr(cfg, 'min_lr', 0) / cfg.learning_rate\n"
"    hold_end = cfg.warmup_steps + getattr(cfg, 'lr_hold_steps', 0)\n"
"\n"
"    def lr_lambda(step):\n"
"        if step < cfg.warmup_steps:\n"
"            return step / max(1, cfg.warmup_steps)\n"
"        if step < hold_end:\n"
"            return 1.0\n"
"        progress = (step - hold_end) / max(1, cfg.max_steps - hold_end)\n"
"        cosine = 0.5 * (1.0 + math.cos(math.pi * progress))\n"
"        return max(min_ratio, cosine)\n"
"\n"
"    scheduler = torch.optim.lr_scheduler.LambdaLR(optimizer, lr_lambda)\n"
"\n"
"    history = {\n"
"        \"step\": [], \"train_ce\": [], \"val_ce\": [],\n"
"        \"train_acc\": [], \"val_acc\": [],\n"
"        \"train_bpc\": [], \"val_bpc\": [],\n"
"        \"sparsity\": [], \"lr\": [],\n"
"        \"step_times\": [], \"per_step_loss\": [],\n"
"    }\n"
"\n"
"    model.train()\n"
"    total_start = time.time()\n"
"    n_params = count_params(model)\n"
"\n"
"    print(f\"\\nTraining {label}: {n_params:,} params\")\n"
"    print(f\"Steps: {cfg.max_steps}, Batch: {cfg.batch_size}, Seq: {cfg.seq_len}\")\n"
"    print(\"=\" * 70)\n"
"\n"
"    for step in range(cfg.max_steps + 1):\n"
"        if step % cfg.eval_interval == 0:\n"
"            res = estimate_loss(model, cfg, is_gpt=is_gpt)\n"
"            tr, vl = res[\"train\"], res[\"val\"]\n"
"            history[\"step\"].append(step)\n"
"            history[\"train_ce\"].append(tr[\"ce\"])\n"
"            history[\"val_ce\"].append(vl[\"ce\"])\n"
"            history[\"train_acc\"].append(tr[\"acc\"])\n"
"            history[\"val_acc\"].append(vl[\"acc\"])\n"
"            history[\"train_bpc\"].append(tr[\"ce\"] / math.log(2))\n"
"            history[\"val_bpc\"].append(vl[\"ce\"] / math.log(2))\n"
"            history[\"sparsity\"].append(vl[\"sparsity\"])\n"
"            history[\"lr\"].append(scheduler.get_last_lr()[0])\n"
"            sp_str = f\" | Sp: {vl['sparsity']:.1%}\" if not is_gpt else \"\"\n"
"            print(f\"[{label}] Step {step:5d} | Train CE: {tr['ce']:.3f} | \"\n"
"                  f\"Val CE: {vl['ce']:.3f} | Val BPC: {vl['ce']/math.log(2):.3f} | \"\n"
"                  f\"Acc: {vl['acc']:.1%}{sp_str}\")\n"
"\n"
"        if step >= cfg.max_steps:\n"
"            break\n"
"\n"
"        step_start = time.time()\n"
"        batch = get_batch(train_data, cfg)\n"
"        optimizer.zero_grad()\n"
"\n"
"        logits, info = model(batch)\n"
"        targets = batch[:, 1:]\n"
"\n"
"        if is_gpt or is_ssm_only:\n"
"            loss = F.cross_entropy(logits.reshape(-1, cfg.vocab_size), targets.reshape(-1))\n"
"        else:\n"
"            # Deep supervision\n"
"            ce_loss, total_weight = 0., 0.\n"
"            for block_logits, weight in info[\"intermediate_logits\"]:\n"
"                ce_loss += weight * F.cross_entropy(\n"
"                    block_logits.reshape(-1, cfg.vocab_size), targets.reshape(-1))\n"
"                total_weight += weight\n"
"            ce_loss /= total_weight\n"
"            # Dictionary coherence loss\n"
"            dcl, nd = 0., 0\n"
"            if hasattr(model, 'blocks'):\n"
"                for blk in model.blocks:\n"
"                    if hasattr(blk, 'memory'):\n"
"                        for dr, di in zip(blk.memory.dict_real, blk.memory.dict_imag):\n"
"                            atoms = torch.fft.irfft(torch.complex(dr, di),\n"
"                                                     n=cfg.subbundle_dim, dim=-1)\n"
"                            An = F.normalize(atoms, dim=-1)\n"
"                            g = An @ An.T\n"
"                            dcl += (g - torch.eye(g.size(0), device=g.device)).pow(2).mean()\n"
"                            nd += 1\n"
"            dcl = dcl / max(nd, 1)\n"
"            loss = ce_loss + 0.1 * dcl\n"
"\n"
"        loss.backward()\n"
"        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)\n"
"        optimizer.step()\n"
"        scheduler.step()\n"
"\n"
"        history[\"step_times\"].append(time.time() - step_start)\n"
"        history[\"per_step_loss\"].append(loss.item())\n"
"\n"
"        if step % 500 == 0 and step > 0:\n"
"            avg_ms = np.mean(history[\"step_times\"][-500:]) * 1000\n"
"            print(f\"    avg step time: {avg_ms:.0f}ms\")\n"
"\n"
"    elapsed = time.time() - total_start\n"
"    avg_ms = np.mean(history[\"step_times\"]) * 1000\n"
"    print(f\"\\n{label} DONE in {elapsed/60:.1f}min | Final BPC: {history['val_bpc'][-1]:.3f} | \"\n"
"          f\"Acc: {history['val_acc'][-1]:.1%} | Avg: {avg_ms:.0f}ms/step\")\n"
"    history[\"avg_step_ms\"] = avg_ms\n"
"    history[\"n_params\"] = n_params\n"
"    return history"
)

