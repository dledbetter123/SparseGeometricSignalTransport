# Cell 5: SpectralTokenEmbedding
code(
"# -- Spectral Token Embedding (half-spectrum) --------------------------------\n"
"\n"
"class SpectralTokenEmbedding(nn.Module):\n"
"    \"\"\"Tokens as sparse spectral configurations (half-spectrum).\n"
"    Magnitude embedding + learned phase + positional phase -> complex spectral -> sparsify.\n"
"    Uses rfft frequencies: each mode is an independent spectral DoF.\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.cfg = cfg\n"
"        self.mag_embedding = nn.Embedding(cfg.vocab_size, cfg.spectral_dim)\n"
"        self.phase_embedding = nn.Embedding(cfg.vocab_size, cfg.spectral_dim)\n"
"        nn.init.uniform_(self.phase_embedding.weight, -math.pi, math.pi)\n"
"\n"
"        freqs = torch.zeros(cfg.spectral_dim)\n"
"        for k in range(cfg.n_subbundles):\n"
"            offset = k * cfg.spectral_half_dim\n"
"            freqs[offset:offset + cfg.spectral_half_dim] = (\n"
"                2 * math.pi * torch.fft.rfftfreq(cfg.subbundle_dim, d=1.0)\n"
"            )\n"
"        self.register_buffer(\"freqs\", freqs)\n"
"\n"
"    def forward(self, token_ids):\n"
"        B, T = token_ids.shape\n"
"        mag = self.mag_embedding(token_ids)\n"
"        phase_offset = self.phase_embedding(token_ids)\n"
"        positions = torch.arange(T, device=token_ids.device).float()\n"
"        pos_phase = positions.unsqueeze(-1) * self.freqs.unsqueeze(0)\n"
"        total_phase = phase_offset + pos_phase.unsqueeze(0)\n"
"        spectral = mag * torch.exp(1j * total_phase)\n"
"        return spectral_sparsify(spectral, self.cfg)"
)

