"# -- Context Accumulator (SSM) -----------------------------------------------\n"
"\n"
"class ContextAccumulator(nn.Module):\n"
"    \"\"\"Content-dependent SSM with parallel scan.\n"
"    q_t = A(x_t) * q_{t-1} + B(x_t) * psi(x_t)\n"
"    fiber_dim -> context_dim (128).\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.A_proj = nn.Linear(cfg.fiber_dim, cfg.context_dim)\n"
