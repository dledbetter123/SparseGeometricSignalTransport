"class GPTNano(nn.Module):\n"
"    def __init__(self, vocab_size=65, n_embd=128, n_head=4, n_layer=12,\n"
"                 block_size=128, dropout=0.1):\n"
"        super().__init__()\n"
"        self.block_size = block_size\n"
"        self.tok_emb = nn.Embedding(vocab_size, n_embd)\n"
