# Cell 25: Summary
md(
"## Summary\n"
"\n"
"### V12.2: Fixed Spectral Machinery\n"
"\n"
"**Three fixes from V12.1 ablation diagnosis:**\n"
"\n"
"1. **rfft/irfft (conjugate redundancy fix):** V12.1 used full fft/ifft. After block 1,\n"
"   FFT of real signal produces conjugate-symmetric spectrum where modes k and N-k have\n"
"   identical magnitude. Top-k selected both, wasting half the sparsity budget.\n"
"   10 selected modes = ~5 independent DoF. With rfft, 10/17 unique modes = 10 independent DoF.\n"
"   *Candes 2006: compressed sensing guarantees require independent measurements.*\n"
"\n"
"2. **Signed diffusion (transport amplification fix):** V12.1 used softplus(D) forcing D>=0,\n"
"   so transport kernel |exp(-D*w^2)| <= 1 everywhere. Transport could only dampen, never\n"
"   amplify. Equivalent to a learnable low-pass filter.\n"
"   *Hoang 2020: GNNs suffer the same low-pass pathology.* tanh allows bidirectional energy flow.\n"
"\n"
"3. **Proximal at end (settler freedom fix):** V12.1 applied spectral proximal at every\n"
"   Langevin step, immediately projecting the settler state back to the sparse spectral\n"
"   manifold and destroying Hopfield gradient progress. Now settler explores freely\n"
"   during settling, projects once at the end.\n"
"   *Ramsauer 2021: Hopfield retrieval needs to reach the attractor, not be yanked away.*\n"
"\n"
"**Non-negotiables (all preserved):**\n"
"1. Sparse in spectral, dense only transiently in spatial\n"
"2. Field reconstruction IS the inverse Fourier transform (irfft)\n"
"3. Langevin starts from the reconstructed (irfft) field\n"
"4. Context warps the spectral metric (D and A are context-dependent)\n"
"5. Spectral proximal enforced (after settling completes)\n"
"6. Subbundles are independent spectral channels\n"
"7. No pairwise token attention\n"
"\n"
"**Block flow:**\n"
"```\n"
"sparse spectral (136) -> irfft -> SSM context -> spectral transport (signed) ->\n"
"irfft (field reconstruction) -> 2-step Hopfield settling (free exploration) ->\n"
"spectral proximal (once) -> SpatialMLP -> gated residual ->\n"
"rfft + re-sparsify -> sparse spectral (136)\n"
"```"
)

