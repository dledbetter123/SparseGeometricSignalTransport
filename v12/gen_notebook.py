"        super().__init__()\n"
"        self.cfg = cfg\n"
"        shd = cfg.spectral_half_dim\n"
"        K = cfg.n_subbundles\n"
"        A = cfg.atoms_per_subbundle\n"
"\n"
"        self.dict_real = nn.ParameterList([\n"
"            nn.Parameter(torch.randn(A, shd) * 0.02) for _ in range(K)\n"
"        ])\n"
"        self.dict_imag = nn.ParameterList([\n"
"            nn.Parameter(torch.randn(A, shd) * 0.02) for _ in range(K)\n"
"        ])\n"
"\n"
"    def get_spatial_atoms(self):\n"
"        \"\"\"irfft: half-spectrum -> real spatial (no .real needed).\"\"\"\n"
"        cfg = self.cfg\n"
"        all_real = torch.stack(list(self.dict_real))\n"
"        all_imag = torch.stack(list(self.dict_imag))\n"
"        all_spatial = F.normalize(\n"
"            torch.fft.irfft(torch.complex(all_real, all_imag),\n"
"                            n=cfg.subbundle_dim, dim=-1),\n"
"            dim=-1\n"
"        )\n"
"        return all_spatial"
