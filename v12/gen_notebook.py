"        self.decoder = nn.Sequential(\n"
"            nn.Linear(cfg.fiber_dim, cfg.fiber_dim), nn.SiLU(),\n"
"            nn.Dropout(cfg.dropout),\n"
"            nn.Linear(cfg.fiber_dim, cfg.vocab_size),\n"
"        )\n"
"        weights = torch.zeros(cfg.n_blocks)\n"
"        for i in range(cfg.n_blocks):\n"
"            if (i + 1) % 2 == 0:\n"
"                weights[i] = (i + 1) / cfg.n_blocks\n"
