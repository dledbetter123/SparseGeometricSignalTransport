# Cell 9: SpectralHopfieldSettler
code(
"# -- Spectral Hopfield Settler (FIX: proximal at end only) -------------------\n"
"\n"
"class SpectralHopfieldSettler(nn.Module):\n"
"    \"\"\"2-step Langevin settling with spectral proximal AFTER final step.\n"
"\n"
"    FIX: V12.1 applied proximal at each Langevin step, projecting state back\n"
"    to sparse spectral manifold and destroying Hopfield gradient progress.\n"
"    Now settler explores freely, projects once at the end.\"\"\"\n"
"\n"
"    def __init__(self, cfg):\n"
"        super().__init__()\n"
"        self.cfg = cfg\n"
"        self.W_inh = nn.Parameter(torch.ones(cfg.fiber_dim) * 0.01)\n"
"\n"
"    def forward(self, dense_field, spatial_atoms):\n"
"        cfg = self.cfg\n"
"        B, T, D = dense_field.shape\n"
"        sd = cfg.subbundle_dim\n"
"        K = cfg.n_subbundles\n"
"        BT = B * T\n"
"\n"
"        x = dense_field\n"
"        betas = torch.linspace(cfg.beta_init, cfg.beta_final, cfg.langevin_steps,\n"
"                               device=dense_field.device)\n"
"        M_all = spatial_atoms.unsqueeze(0).expand(BT, -1, -1, -1)\n"
"\n"
"        for step in range(cfg.langevin_steps):\n"
"            beta = betas[step].item()\n"
"            x_subs = x.reshape(BT, K, sd)\n"
"            sim = torch.einsum('bks,bkas->bka', x_subs, M_all)\n"
"            w = F.softmax(beta * sim, dim=-1)\n"
"            grad_E = -torch.einsum('bka,bkas->bks', w, M_all)\n"
"            inhib = self.W_inh * x\n"
"            x = x - cfg.langevin_lr * (grad_E.reshape(B, T, D) + inhib)\n"
"            if not self.training:\n"
"                x = x + math.sqrt(2.0 * cfg.langevin_lr / beta) * torch.randn_like(x)\n"
"\n"
"        # Spectral proximal ONLY after final step\n"
"        x = spectral_proximal(x, cfg)\n"
"        return x"
)

